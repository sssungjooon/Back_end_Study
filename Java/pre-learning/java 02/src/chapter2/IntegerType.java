package chapter2;

public class IntegerType {
	
	// static : 객체를 생성하지 않고도 호출 가능하게 한다.
	public static void main(String[] args) {
		byte b = 10;
		short s = 20;
		int i = 1000;
		long l = 10000000000L;
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
	}

}
